class Operators {

 public static void main(String[] args) {
  
  int girls, boys, all;
  girls=60; boys=7;
            
            all= girls+boys;
            System.out.println("+ = " +all );

            all= girls-boys;
            System.out.println("- = " +all );

            all= girls*boys;
            System.out.println("* = " +all );

            all= girls/boys;
            System.out.println("/ = " +all );

            all= girls%boys;
            System.out.println("% = " +all );

 }

}
